-- Create profile + initial locked collection rows when auth.users row is created.
create or replace function public.handle_new_auth_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, email, preferred_model)
  values (new.id, coalesce(new.email, ''), 'model00000')
  on conflict (id) do update
  set email = excluded.email;

  insert into public.user_characters (user_id, character_id, unlocked, unlocked_at)
  select new.id, c.id, false, null
  from public.characters c
  on conflict (user_id, character_id) do update
  set
    unlocked = excluded.unlocked,
    unlocked_at = coalesce(public.user_characters.unlocked_at, excluded.unlocked_at);

  insert into public.user_cards (user_id, card_id, unlocked, unlocked_at)
  select new.id, c.id, false, null
  from public.cards c
  on conflict (user_id, card_id) do nothing;

  return new;
end;
$$;

drop trigger if exists trg_auth_user_created on auth.users;
create trigger trg_auth_user_created
after insert on auth.users
for each row
execute function public.handle_new_auth_user();


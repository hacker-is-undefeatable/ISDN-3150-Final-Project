-- IMPORTANT: this function is called from user_cards unlock trigger and can be called manually.
create or replace function public.unlock_character_if_completed(
  p_user_id uuid,
  p_character_id smallint
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_total_cards integer;
  v_unlocked_cards integer;
begin
  select count(*)
  into v_total_cards
  from public.cards c
  where c.character_id = p_character_id;

  if v_total_cards = 0 then
    return;
  end if;

  select count(*)
  into v_unlocked_cards
  from public.user_cards uc
  join public.cards c on c.id = uc.card_id
  where uc.user_id = p_user_id
    and c.character_id = p_character_id
    and uc.unlocked = true;

  if v_unlocked_cards = v_total_cards then
    update public.user_characters uch
    set unlocked = true,
        unlocked_at = coalesce(uch.unlocked_at, timezone('utc', now()))
    where uch.user_id = p_user_id
      and uch.character_id = p_character_id
      and uch.unlocked = false;
  end if;
end;
$$;

create or replace function public.handle_user_card_unlock_trigger()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_character_id smallint;
begin
  if new.unlocked = true and (tg_op = 'INSERT' or old.unlocked is distinct from true) then
    select c.character_id
    into v_character_id
    from public.cards c
    where c.id = new.card_id;

    if v_character_id is not null then
      perform public.unlock_character_if_completed(new.user_id, v_character_id);
    end if;
  end if;

  return new;
end;
$$;

create or replace function public.handle_user_card_shard_unlock()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if new.shard_count >= 4 and new.unlocked = false then
    new.unlocked := true;
    new.unlocked_at := coalesce(new.unlocked_at, timezone('utc', now()));
  elsif new.shard_count < 4 and new.unlocked = true then
    new.unlocked := false;
    new.unlocked_at := null;
  end if;

  return new;
end;
$$;

